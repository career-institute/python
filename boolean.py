# Booleans in Python
# Booleans represent one of two values: True or False.

# Syntax
# print(True)
# print(False)

# Boolean values
print(10 > 9) # True
print(10 == 9) # False
print(10 < 9) # False

# if a variable has a value, it is True
bool("abc")
bool(123)
bool(["apple", "cherry", "banana"])

# if a variable is empty, it is False
bool(False)
bool(None)
bool(0)
bool("")
bool(())
bool([])
bool({})