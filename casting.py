# Casting in Python
# Casting is when you change the type of a variable to another type.

# Example 1: Casting an integer to a float
x = int(1)   # x will be 1
y = float(1) # y will be 1.0
z = str(1)   # z will be '1'

# Example 2: Casting a float to an integer
x = int(1.0)   # x will be 1
y = float(1.0) # y will be 1.0
z = str(1.0)   # z will be '1.0'

# Example 3: Casting a string to an integer
x = int('1')   # x will be 1
y = float('1') # y will be 1.0
z = str('1')   # z will be '1'

